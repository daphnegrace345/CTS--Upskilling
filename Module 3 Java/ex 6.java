// Data Type Demonstration
class DataTypesDemo {
    public static void main(String[] args) {

        // Declaring variables
        int number = 100;
        float price = 99.5f;
        double salary = 45678.90;
        char grade = 'A';
        boolean isJavaEasy = true;

        // Displaying values
        System.out.println("Integer value: " + number);
        System.out.println("Float value: " + price);
        System.out.println("Double value: " + salary);
        System.out.println("Character value: " + grade);
        System.out.println("Boolean value: " + isJavaEasy);
    }
}
